import mongoose from 'mongoose';

const TasksSchema = new mongoose.Schema({
  _id: {
    type: mongoose.Types.ObjectId,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
});

const Tasks = mongoose.model('Tasks', TasksSchema);

export default Tasks;
